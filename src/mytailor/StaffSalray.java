     public void showInfo( JTable info){
    try{
            DBconnection db=new DBconnection(); 
            String q="select * from assignment";
            Statement st=db.conn.createStatement();
            ResultSet rs=st.executeQuery(q);
         // info.setModel(DbUtils.resultSetToTableModel(rs));
          ///////////////////////\\\\\\\\\\\\\\\\\\\\\\\\
           DefaultTableModel model = new DefaultTableModel();
          String[] columnNames = {"ID","Order ID","Item", "Quantity", "Assign to","Assign Date","Delivery Date","Status","Amount"};
          model.setColumnIdentifiers(columnNames);
          info.setModel(model);
       String id = "";
        String item = "";
        String qty = "";
        String asTo = "";
        String  aDate= "";
         String dDate = "";
         String status = "";
         String price = "";
         String ord="";
          
           while(rs.next()) {
                id = rs.getString("ID");
                ord=rs.getString("ordno");
                item= rs.getString("assign_item");
                qty = rs.getString("item_qty");
                asTo = rs.getString("assign_staff");
                aDate = rs.getString("assign_date");
                dDate = rs.getString("delivey_date");
                status = rs.getString("status");
                price = rs.getString("cust_rate");
                model.addRow(new Object[]{id,ord,item,qty,asTo,aDate,dDate,status,price});
                
            
            
            }
         
    }catch(SQLException e){}
}
    
    
