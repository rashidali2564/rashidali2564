     public void showTable( JTable info){
      
    try{
            DBconnection db=new DBconnection(); 
            String q="select * from   materialExpenses";
            Statement st=db.conn.createStatement();
            ResultSet rs=st.executeQuery(q);
         
           DefaultTableModel model = new DefaultTableModel();
                 String[] columnNames = {"Serial No.","Item","Quantity","Rate â‚¹","Total cost","Description"};
                 model.setColumnIdentifiers(columnNames);
                 info.setModel(model);
                 String sno = "";

                 String item = "";
                 String qty = "";
                 String  rt= "";
                 String tot= "";
                 String desc="";
                 
   
         
            
           while(rs.next()) {
                sno = rs.getString("Sno");
                item= rs.getString("item_name");
                qty = rs.getString("qty");
                rt= rs.getString("cost_per_item");
                tot = rs.getString("total_cost");
                desc=rs.getString("desc");
                        
                model.addRow(new Object[]{sno,item,qty,rt,tot,desc});
                       
               }
           
    }catch(SQLException e){}
}
